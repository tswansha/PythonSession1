mylist=[1,2,3,4,5,6,7]
mystring="Hi I am Thilina"
mytuple=(1,2,3,4,5,6,7)

#Lets print whole object
print("mylist",mylist)
print("mystring",mystring)
print("mytuple",mytuple)

#Access first element of object
print("mylist[0]",mylist[0])
print("mystring[0]",mystring[0])
print("mytuple[0]",mytuple[0])

#Access any element of object
print("mylist[5]",mylist[5])
print("mystring[7]",mystring[7])
print("mytuple[7]",mytuple[5])

#Use Negative index
print("mylist[-1]",mylist[-1])
print("mystring[-3]",mystring[-3])
print("mytuple[-7]",mytuple[-7])
